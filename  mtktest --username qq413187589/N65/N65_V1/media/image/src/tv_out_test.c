/**
 * To get test item information
 *
 * @param index      test item
 * @param item_name  test item name pointer
 * @param info_ptr   test item structure pointer
 *
 * @return None
 */
void get_tv_engmode_item_info(TV_ENGMODE_ITEM index, kal_uint8 *item_name, tv_engmode_info *info_ptr)
{
   /*return max/min values */
	switch (index)
	{
		case TV_START_PIXEL_OFFSET_X:/*qu720x576(PAL)*/
			sprintf((char*)item_name, "TV start pixel X offset");
			info_ptr->current_value= tv_engmode_start_x ;
			info_ptr->min_value=0;
			info_ptr->max_value=720;
		break;
		case TV_START_PIXEL_OFFSET_Y:
			sprintf((char*)item_name, "TV start pixel Y offset");
			info_ptr->current_value= tv_engmode_start_y;
			info_ptr->min_value=21;/*HW limit*/
			info_ptr->max_value=576;
		break;
		case TV_U_SCALE:/*8 bits*/
			sprintf((char*)item_name, "TV U scale");
			info_ptr->current_value= tv_engmode_u_scale;
			info_ptr->min_value=0;
			info_ptr->max_value=0xFF;
		break;
		case TV_V_SCALE:/*8 bits*/
			sprintf((char*)item_name, "TV V scale");
			info_ptr->current_value= tv_engmode_v_scale;
			info_ptr->min_value=0;
			info_ptr->max_value=0xFF;
		break;
		case TV_Y_DELAY:/*3 bits*/
			sprintf((char*)item_name, "TV Y delay");
			info_ptr->current_value= tv_engmode_y_delay;
			info_ptr->min_value=0;
			info_ptr->max_value=7;
		break;
		case TV_COLOR_BURST_LEVEL:/*7bits, spec is wrong*/
			sprintf((char*)item_name, "TV color burst level");
			info_ptr->current_value= tv_engmode_burst_level;
			info_ptr->min_value=0;
			info_ptr->max_value=0x7F;
		break;
		case TV_GAMMA_TABLE_SEL:/*5 gamma table*/
			sprintf((char*)item_name, "TV gamma table");
			info_ptr->current_value= tv_engmode_gamma_table;
			info_ptr->min_value=0;
			info_ptr->max_value=5;
		break;
		case TV_TEST_PATTERN_SEL:/*6 test patterns*/
			sprintf((char*)item_name, "TV test pattern");
			info_ptr->current_value= tv_engmode_test_pattern;
			info_ptr->min_value=0;
			info_ptr->max_value=6; 
		break;
		case TV_FORMAT_SEL:/*NTSC or PAL*/
			sprintf((char*)item_name, "NTSC(0)/PAL(1)");
			info_ptr->current_value= TV_FORMAT_NTSC;
			info_ptr->min_value=0;
			info_ptr->max_value=1;
		break;
	}
}

/**
 * To adjust YUV component
 *
 * @param type  test item
 * @param para  test item name pointer 
 *
 * @return None
*/
void set_tv_parameter(TV_OUT_COMP_ENUM type,kal_uint8 para)
{
	switch (type)
	{
		case TV_OUT_Y_COMP:
			if ((para>=TV_OUT_GAMMA1) && (para<=TV_OUT_GAMMA5))
			{
				SET_TV_ENC_GAMMA0_COEFF(tv_gamma_table[para][0]);
				SET_TV_ENC_GAMMA1_COEFF(tv_gamma_table[para][1]);
				SET_TV_ENC_GAMMA2_COEFF(tv_gamma_table[para][2]);
				SET_TV_ENC_GAMMA3_COEFF(tv_gamma_table[para][3]);
				SET_TV_ENC_GAMMA4_COEFF(tv_gamma_table[para][4]);
				SET_TV_ENC_GAMMA5_COEFF(tv_gamma_table[para][5]);
				SET_TV_ENC_GAMMA6_COEFF(tv_gamma_table[para][6]);
				SET_TV_ENC_GAMMA7_COEFF(tv_gamma_table[para][7]);
				SET_TV_ENC_GAMMA8_COEFF(tv_gamma_table[para][8]);
				tv_gamma_table_sel=para;
			}
			else
				ASSERT(0);  
		break;
		case TV_OUT_U_COMP:
			if (tv_eng_mode==KAL_FALSE)
			{
				SET_TV_ENC_U_SCALE(para);
				tv_u_scale_ntsc=para;
				tv_u_scale_pal=para;
			}
		break;
		case TV_OUT_V_COMP:
			if (tv_eng_mode==KAL_FALSE)
			{
				SET_TV_ENC_V_SCALE(para);
				tv_v_scale_ntsc=para;
				tv_v_scale_pal=para;
			}
		break;
	}
}	/* set_tv_parameter() */
